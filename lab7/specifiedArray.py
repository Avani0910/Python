# g. Write a NumPy program to find the 4th element of a specified array.

import numpy as np

# Create an example array
arr = np.array([10, 20, 30, 40, 50, 60])

# Access the 4th element (index 3, since indexing starts at 0)
fourth_element = arr[3]

print("The 4th element of the array is:", fourth_element)
